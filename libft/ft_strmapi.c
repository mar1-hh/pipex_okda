#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int				size;
	char			*ptr;
	unsigned int	i;

	if (!s || !f)
		return (NULL);
	size = ft_strlen(s);
	i = 0;
	ptr = malloc(size + 1);
	if (!ptr)
		return (NULL);
	while (s[i])
	{
		ptr[i] = f(i, s[i]);
		i++;
	}
	ptr[i] = 0;
	return (ptr);
}
